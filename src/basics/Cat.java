package basics;

public class Cat {
    private String name;
    private int year;
    private String race;

    public String getName() {
	return name;
    }

    public void setName(String name) {
	this.name = name;
    }

    public int getYear() {
	return year;
    }

    public void setYear(int year) {
	this.year = year;
    }

    public String getRace() {
	return race;
    }

    public void setRace(String race) {
	this.race = race;
    }

}
